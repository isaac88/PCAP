print(2 ** 3) # Result Int
print(2 ** 3.) # Result Float
print(2. ** 3) # Result Float
print(2. ** 3.) # Result Float

# Multiplication
print(2 * 3)
print(2 * 3.) # Result Float
print(2. * 3) # Result Float
print(2. * 3.) # Result Float

# Division (Always result Float)
print(6 / 3) # Result Float
print(6 / 3.) # Result Float
print(6. / 3) # Result Float
print(6. / 3.) # Result Float

# Floor division
print(6 // 3) # Result Int
print(6 // 3.) # Result Float
print(6. // 3) # Result Float
print(6. // 3.) # Result Float

print(6 // 4) # = 1 Result Int
print(6. // 4) # = 1.0 Result Float

print(6 / 4) # = 1.5 Result Float
print(6. / 4) # = 1.5 Result Float

# Rounding is done towards the lower integer value
print(-6 // 4)
print(6. // -4)

# Modulus
print(12 % 4.5)

# Sum
print(-4 + 4)
print(-4. + 8)
print(-4 - 4)
print(4. - 8)
print(-1.1)
print(+2)
